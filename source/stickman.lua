STICKMAN_LERP=4
STICKMAN_X=120
STICKMAN_Y=67

stickman_st=0
function stickman_init()
	stickman_st=_t
	sweetie16_init()
	salad(1,{fig,apple},2000,2000)
	salad(2,{cherry,pineapple},4000,2000)
	salad(3,{banana,blueberry},2200,2000)
	salad(4,{strawberry,avocado,mango,fig},2500,2000)
end

function stickman()
	cls()
	local t=(_t-stickman_st)/30
	local f=(t//STICKMAN_LERP)%(#stickman_data-1) -- whole frame
	local r=t%STICKMAN_LERP -- fraction
  -- interpolated coords:
	local X={}
	local Y={}
	for i=1,12 do
		-- current frame
		local ox=stickman_data[f+1][1][i]
		local oy=stickman_data[f+1][2][i]
		-- next frame deltas
		local dx=stickman_data[f+2][1][i]-ox
		local dy=stickman_data[f+2][2][i]-oy
		-- interpolate
		X[i]=ox+dx/STICKMAN_LERP*r
		Y[i]=oy+dy/STICKMAN_LERP*r
	end

	-- draw figure
	line(STICKMAN_X+X[0+1],STICKMAN_Y+Y[0+1],STICKMAN_X+X[1+1],STICKMAN_Y+Y[1+1],12)
	line(STICKMAN_X+X[1+1],STICKMAN_Y+Y[1+1],STICKMAN_X+X[2+1],STICKMAN_Y+Y[2+1],12)
	line(STICKMAN_X+X[2+1],STICKMAN_Y+Y[2+1],STICKMAN_X+X[0+1],STICKMAN_Y+Y[0+1],12)
	line(STICKMAN_X+X[0+1],STICKMAN_Y+Y[0+1],STICKMAN_X+X[3+1],STICKMAN_Y+Y[3+1],12)
	line(STICKMAN_X+X[3+1],STICKMAN_Y+Y[3+1],STICKMAN_X+X[4+1],STICKMAN_Y+Y[4+1],12)
	line(STICKMAN_X+X[0+1],STICKMAN_Y+Y[0+1],STICKMAN_X+X[5+1],STICKMAN_Y+Y[5+1],12)
	line(STICKMAN_X+X[5+1],STICKMAN_Y+Y[5+1],STICKMAN_X+X[6+1],STICKMAN_Y+Y[6+1],12)
	line(STICKMAN_X+X[0+1],STICKMAN_Y+Y[0+1],STICKMAN_X+X[7+1],STICKMAN_Y+Y[7+1],12)
	line(STICKMAN_X+X[7+1],STICKMAN_Y+Y[7+1],STICKMAN_X+X[8+1],STICKMAN_Y+Y[8+1],12)
	line(STICKMAN_X+X[8+1],STICKMAN_Y+Y[8+1],STICKMAN_X+X[9+1],STICKMAN_Y+Y[9+1],12)
	line(STICKMAN_X+X[7+1],STICKMAN_Y+Y[7+1],STICKMAN_X+X[10+1],STICKMAN_Y+Y[10+1],12)
	line(STICKMAN_X+X[10+1],STICKMAN_Y+Y[10+1],STICKMAN_X+X[11+1],STICKMAN_Y+Y[11+1],12)

	local bg=f>108 and stickman_bg2 or stickman_bg1
