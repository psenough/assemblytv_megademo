	for i=1,#bg[1],2 do
		line(STICKMAN_X+bg[1][i],STICKMAN_Y+bg[2][i],STICKMAN_X+bg[1][i+1],STICKMAN_Y+bg[2][i+1],12)
	end

	overlay_defaults((_t-stickman_st),"98","COMA")
end

